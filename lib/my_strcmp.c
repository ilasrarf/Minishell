#include "../minishell.h"

int	my_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	if (!s1 || !s2)
		return (0);
	i = 0;
	j = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			break ;
		i++;
	}
	if (!s2[i] && s1[i] == '=')
		return (1);
	return (0);
}
